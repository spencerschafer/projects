#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*chr;

	i = 0;
	chr = s;
	while (i < n)
	{
		*chr = 0;
		i++;
		chr++;
	}
}
